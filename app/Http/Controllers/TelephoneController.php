    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create($user_id, Request $request)
    {
         $this->validate($request, [
             'telephone' => 'required',
             'user_id'    => 'required'
         ]);

         $telephone = new Telephon();

         $telephone->telephone = $request->telephone;
         $telephone->User_id  = $request->user_id;

         $telephone->save();
         $request->session()->flash('flash_message', 'Telephone successfully added!');

         return redirect()->back();

    }

